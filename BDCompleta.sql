  CREATE OR REPLACE PROCEDURE "NOMASACCIDENTES"."SPP_LISTAR_PAGOS" (ucursor out sys_refcursor)
is
begin
open ucursor for
select c.rut_cliente,c.fecha_contrato, c.fecha_vencimiento,ADD_MONTHS(c.fecha_contrato,6)as "primer pago",  ADD_MONTHS(c.fecha_contrato,12)as "segundo pago"
from  contrato c
inner join SERVICIOS_EN_CONTRATOS sc
on c.ID_SERVICIOS_EN_CONTRATOS = sc.ID_SERVICIOS_EN_CONTRATOS
inner join servicios s
on sc.id_servicios =s.id_servicios
inner join detalles_documentos d
on d.id_detalles_documentos=s.id_detalles_documentos
inner join documentos doc
on doc.id_documentos=d.id_documentos
inner join pagos p
on p.id_pagos = doc.id_pagos
;
end spp_listar_pagos;

