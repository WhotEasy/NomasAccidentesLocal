v_nombre_profesional profesional.nombre_profesional%TYPE; 
v_nombre_usuario usuario.nombre_usuario%TYPE;
v_contrasena usuario.contrasena%TYPE;
v_id_roles_usuarios usuario.id_roles_usuarios%TYPE; 
v_id_estado_usuarios usuario.id_estado_usuarios%TYPE;
